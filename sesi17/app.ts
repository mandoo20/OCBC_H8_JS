var valueA = 10;
var valueB = 2;

var sum = valueA + valueB;
var difference = valueA - valueB;
var product = valueA * valueB;
var quotient = valueA / valueB;
var remainder = valueA % valueB;
valueA++;
valueB--;

console.log("Sum: " + sum);
console.log("Difference: " + difference);
console.log("Product: " + product);
console.log("Quotient: " + quotient);
console.log("Remainder: " + remainder);
console.log("Value of num1 after increment: " + valueA);
console.log("Value of num2 after decrement: " + valueB);
